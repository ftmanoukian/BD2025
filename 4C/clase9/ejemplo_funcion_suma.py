# Creo una función que suma dos números

# Definición
def mi_funcion_suma(num1, num2):
    resultado = num1 + num2
    return resultado

# Llamado y guardado del resultado
res = mi_funcion_suma(7,33)

# Muestro el resultado en el terminal
print(res)

# Otra forma de llamar a la función
print(mi_funcion_suma(2,50))
print(mi_funcion_suma(1,100))